%colors
green=[0.4660 0.6740 0.1880];
red=[0.6350 0.0780 0.1840];
blue=[0 0.4470 0.7410];

n=5;
x=[1; 10; 100; 1000; 10000];

ytarg=[mean(muldt_targ(1:10,:));...
    mean(muldt_targ(11:20,:));...
    mean(muldt_targ(21:30,:));...
    mean(muldt_targ(31:40,:));...
    mean(muldt_targ(41:50,:))];

yran=[mean(muldt_rand(1:10,:));...
    mean(muldt_rand(11:20,:));...
    mean(muldt_rand(21:30,:));...
    mean(muldt_rand(31:40,:));...
    mean(muldt_rand(41:50,:))];

ytargsd=[std(muldt_targ(1:10,:));...
    std(muldt_targ(11:20,:));...
    std(muldt_targ(21:30,:));...
    std(muldt_targ(31:40,:));...
    std(muldt_targ(41:50,:))];

yrans=[std(muldt_rand(1:10,:));...
    std(muldt_rand(11:20,:));...
    std(muldt_rand(21:30,:));...
    std(muldt_rand(31:40,:));...
    std(muldt_rand(41:50,:))];

cDdes=cbar*1e6*ones(n,1);
mulD0=[(10^0)*(2e2);(10^1)*(2e2);(10^2)*(2e2);(10^3)*(2e2);(10^4)*(2e2)];
goal=cDdes-mulD0;

h1=plot(x,goal,'-','color',blue,'LineWidth',2); hold;

plot(x,ytarg(:,4),'-','color',green,'LineWidth',2);
h4=plot(x,ytarg(:,4)+ytargsd(:,4),'-.','color',green,'LineWidth',1);
plot(x,ytarg(:,4)-ytargsd(:,4),'-.','color',green,'LineWidth',1);
for i=1:n
    h2=plot(x(i),ytarg(i,4),'o','color',green,'LineWidth',2);
end

plot(x,yran(:,4),'-','color',red,'LineWidth',2);
h5=plot(x,yran(:,4)+yrans(:,4),'-.','color',red,'LineWidth',1);
plot(x,yran(:,4)-yrans(:,4),'-.','color',red,'LineWidth',1);
for i=1:n
    h3=plot(x(i),yran(i,4),'o','color',red,'LineWidth',2);
end

ylim([5e6 5e10]);
set(gca,'Yscale','log');
set(gca,'Xscale','log');

xlabel('$\alpha_i$','Interpreter','latex','FontSize',18);
ylabel('Achieved $D_{T_{11}}$','Interpreter','latex','FontSize',18);
legend([h2,h4,h3,h5,h1],{'$\mu(D_{T_{T,i,11}})$',...
    '$\pm \sigma(D_{T_{T,i,11}})$',...
    '$\mu(D_{T_{R,i,11}})$',...
    '$\pm \sigma(D_{T_{R,i,11}})$',...
    '$\overline{D}_{T_{11}}$'}, ...
    'Interpreter','latex','FontSize',13,'Location','southeast');
